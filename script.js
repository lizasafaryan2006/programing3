let side = 30
var grassArr = [];
var xaxacorArr = [];

function setup() {
    createCanvas(matrix.length * side, matrix[0].length * side)
    background(200)
    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[x].length; x++) {
            if (matrix[y][x] == 2) {
                var grass1 = new Grass(x, y)
                grassArr.push(grass1)
            } else if (matrix[y][x] == 3) {
                var xaxacox1 = new Xaxacox(x,y)
                xaxacorArr.push(xaxacox1)
            }
        }
    }
}




function draw() {

    for (let x = 0; x < matrix.length; x++) {
        for (let y = 0; y < matrix[x].length; y++) {
            if (matrix[x][y] === 0) {
                fill("grey")
            } else if (matrix[x][y] === 1) {
                fill("white")
            } else if (matrix[x][y] === 2) {
                fill("green")

            } else if (matrix[x][y] === 3) {
                fill("yellow")
            }

            rect(x * side, y * side, side, side)


        }
    }


    for (let i = 0; i < grassArr.length; i++) {
        grassArr[i].mult();
    }
    for (let i = 0; i < xaxacorArr.length; i++) {
        xaxacorArr[i].move();
    }

}
